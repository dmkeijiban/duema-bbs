create or replace view public.maker_tier_aggregates as  SELECT p.id AS project_id,
    c.id AS card_id,
    c.name,
    count(*) FILTER (WHERE i.group_key = 's'::text)::integer AS s_count,
    count(*) FILTER (WHERE i.group_key = 'a'::text)::integer AS a_count,
    count(*) FILTER (WHERE i.group_key = 'b'::text)::integer AS b_count,
    count(*) FILTER (WHERE i.group_key = 'c'::text)::integer AS c_count,
    count(*) FILTER (WHERE i.group_key = 'd'::text)::integer AS d_count,
    count(DISTINCT s.user_id)::integer AS rating_count,
    avg(
        CASE i.group_key
            WHEN 's'::text THEN 5
            WHEN 'a'::text THEN 4
            WHEN 'b'::text THEN 3
            WHEN 'c'::text THEN 2
            WHEN 'd'::text THEN 1
            ELSE NULL::integer
        END)::numeric(5,2) AS average_tier
   FROM maker_projects p
     JOIN maker_submissions s ON s.project_id = p.id AND s.is_valid
     JOIN maker_submission_items i ON i.submission_id = s.id
     JOIN cards c ON c.id = i.card_id
  GROUP BY p.id, c.id, c.name;;
create or replace view public.maker_selection_aggregates as  SELECT p.id AS project_id,
    c.id AS card_id,
    count(DISTINCT i.submission_id)::integer AS selection_count,
    count(DISTINCT s.id)::integer AS submission_count,
        CASE
            WHEN count(DISTINCT s.id) = 0 THEN 0::numeric
            ELSE round(count(DISTINCT i.submission_id)::numeric / count(DISTINCT s.id)::numeric * 100::numeric, 1)
        END AS selection_rate
   FROM maker_projects p
     JOIN maker_project_cards pc ON pc.project_id = p.id
     JOIN cards c ON c.id = pc.card_id
     LEFT JOIN maker_submissions s ON s.project_id = p.id AND s.is_valid
     LEFT JOIN maker_submission_items i ON i.submission_id = s.id AND i.card_id = c.id AND i.group_key = 'release'::text
  GROUP BY p.id, c.id;;
